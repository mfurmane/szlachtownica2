package priv.mfurmane.szlachtownica.model.simulation.goal;

public enum Goal {
    //💰 Materializm / Status / Ambicja
//Cele związane z dobrami materialnymi, statusem społecznym i prestiżem.
    GREED, // – dążenie do bogactwa i jego pomnażania dla samego bogactwa
    LUXURY, // – dążenie do przynależenia do bogatej gałęzi rodziny dla wygody
    CHILDREN_WEALTH, // – chęć zapewnienia dzieciom bogactwa
    CHILDREN_STATUS, // – chęć zapewnienia dzieciom statusu społecznego
    PRESTIGE, // – dążenie do uznania i bycia podziwianym, w tym atrakcyjny małżonek, czy publiczne kochanki
    RESPECT, // – dążenie do uznania/autorytetu
    RECOGNITION, // – sława, reputacja
    CONTROLLED_IMAGE, // – dbałość o pozory i reputację
    //🏛️ Władza / Kontrola / Wpływ
//Cele związane z dominacją, kontrolą nad sobą lub innymi.
    POWER, // – dążenie do poszerzania swoich wpływów i rzeczywistej władzy
    INFLUENCE, // – miękka władza nad innymi
    CONTROL, // – kontrolowanie innych/sytuacji
    DESTABILIZATION, // – destabilizacja sprzyja nagłym i dużym zmianom, można wypłynąć
    STABILIZATION, // – dążenie do stabilnośći i bezpieczeństwa i jego zachowania na szeroką skalę
    SECURITY, // – unikanie ryzyka, dążenie do osobistej stabilizacji
    VENGEANCE, // – dążenie do odwetu
    //❤️ Relacje / Miłość / Przynależność
//Cele społeczne i emocjonalne, oparte na relacjach międzyludzkich.
    LOVE, // – romantyzm, poszukiwanie miłości większej niż życie
    LUST, // – głównym motorem napędowym jest popęd, atrakcyjność partnerów ważna
    BELONGING, // – potrzeba przynależności
    DYNASTY_SURVIVE, // – dążenie do oficjalnej reprodukcji i przekazania nazwiska
    REPRODUCTION, // – prymitywna potrzeba mnożenia się
    BLOODLINE_PURITY, // – czystość rasowa/dynastyczna
    //🧠 Intelekt / Duchowość / Pamięć
//Cele wyższego rzędu, poznawcze, ideowe i związane z dziedzictwem.
    KNOWLEDGE, // – chęć poznania, studiowania, mistycyzm
    LEGACY, // – pozostawienie śladu w historii
    FAITH, // – religijność, mistyka, ideologiczna gorliwość
    //🧍‍♂️ Autonomia / Samorealizacja / Przetrwanie
//Cele związane z przetrwaniem, niezależnością i spełnieniem wewnętrznym.
    PERSONAL_SURVIVE, // – chęć przetrwania
    CALMNESS, // – dążenie do spokoju, niezależnie od własnego statusu
    FUN, // – chęć ciągłej zabawy, imprezy, bale, orgie, gry, podróże
    CHAOS, // – głównym motorem napędowym jest nuda i chęć, żeby coś się działo
    ATTENTION, // – chęć ciągłego bycia w centrum uwagi
    FREEDOM, // – niechęć do zobowiązań i struktur
    INDEPENDENCE, // – unikanie zależności

}
